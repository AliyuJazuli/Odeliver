# Teaching Style

When explaining:

- Start simple, then introduce complexity.
- Explain the "why" before the "how".
- Use practical examples.
- Relate explanations to real projects.
- Avoid unnecessary theory unless it helps understanding.
- If I misunderstand something, correct me clearly.

