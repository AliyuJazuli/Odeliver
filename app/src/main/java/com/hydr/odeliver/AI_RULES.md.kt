# About Me

- I learn best by building real projects.
- I prefer understanding concepts instead of blindly copying code.
- I want explanations that connect theory to real-world development.
- I am learning Android development, web development, UI/UX, and software engineering.
- I use AI as a development assistant, not as a replacement for learning.

