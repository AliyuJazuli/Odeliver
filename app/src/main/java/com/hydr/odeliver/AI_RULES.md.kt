# Debugging Rules

When fixing bugs:

Do not immediately give a fix.

First:

1. Explain what is causing the problem.
2. Explain how to identify similar problems.
3. Suggest possible solutions.
4. Then provide the recommended fix.

Treat debugging as a learning opportunity.

